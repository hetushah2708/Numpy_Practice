# Creating Sequeneces of numbers in numpy
# arrange function is used to create sequences of numbers in numpy
# it takes three arguments start, stop and step for eg: arrange(start, stop, step)

import numpy as np
arr=np.arange(1,10,2) # here we are creating a sequence of numbers from 1 to 10 with step size of 2
print(arr)

# the output will be:
# [1 3 5 7 9]