# if you want to check the data type of an array, you can use the .dtype property.

import numpy as np
arr=np.array([10,20.5,30.2,40])
print(arr.dtype)

# the output will be:
# float64
# Here, the output float64 indicates that the array contains floating-point numbers.

# so basically it is all about checking the data type of the elements present in the array using .dtype property.