import numpy as np

arr= np.array([10,20,30])

print(arr + 5)
print(arr * 2)
print(arr ** 2)

# the output will be:
# [15 25 35]
# [20 40 60]
# [1000 400 900]
# Here, we are performing element-wise arithmetic operations on the NumPy array 'arr'.
# here it becomes very easy to perform arithmetic operations on entire arrays without the need for explicit loops.