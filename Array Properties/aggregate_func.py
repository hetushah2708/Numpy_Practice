import numpy as np

arr = np.array([10,20,30,40,50])

print(np.sum(arr)) # this will give the sum of all elements in the array
print(np.mean(arr)) # this will give the mean of all elements in the array
print(np.min(arr)) # this will give the minimum value from the array
print(np.max(arr)) # this will give the maximum value from the array
print(np.std(arr)) # this will give the standard deviation of the array elements
print(np.var(arr)) # this will give the variance of the array elements

# the output will be:
# 150
# 30.0
# 10
# 50
# 14.142135623730951
# 200.0
