# Indesxing in NumPy is used to access specific elements, rows, columns, or subarrays from a NumPy array.
'''
array[index] for 1D arrays
array[row, column] for 2D arrays
'''
import numpy as np

arr=np.array([10,20,30,40,50])
print(arr[0]) # Accessing the first element of a 1D array ie 10
print(arr[2]) # Accessing the third element of a 1D array ie 30
print(arr[-1]) # Accessing the last element of a 1D array ie 50
             